// Şehir, ilçe ve mahalle listelerini dinamik olarak oluştur
export const getLocations = () => {
  const cities = [...new Set(metersData.map(m => m.city))];
  const districts = {};
  const neighborhoods = {};

  cities.forEach(city => {
    districts[city] = [...new Set(metersData.filter(m => m.city === city).map(m => m.district))];
    
    districts[city].forEach(district => {
      const key = `${city}-${district}`;
      neighborhoods[key] = [...new Set(metersData.filter(m => m.city === city && m.district === district).map(m => m.neighborhood))];
    });
  });

  return { cities, districts, neighborhoods };
};
